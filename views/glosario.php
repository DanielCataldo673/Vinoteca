        <div class="accordion-body">
          <strong>Vinificación :</strong> El elemento que más influye en el vino, más incluso que la variedad de la uva, es el método que se utiliza para elaborar este. Para empezar, hay muchos tipos de vino: tintos, blancos, rosados, espumosos, generosos…
          La elaboración de vino se puede dividir en tres etapas: en la primera, se obtiene el mosto de las uvas maduras, a través del tradicional prensado u otros métodos; después el mosto es fermentado por levaduras que consumen azúcar y producen alcohol, formando vino nuevo; la tercera etapa es el envejecimiento o maduración de este (de la que hablaremos más adelante).Hoy en día los métodos de elaboración del vino están muy estudiados e influyen enormemente, más que cualquier otra variable, en el resultado final. De poco sirve tener las mejores uvas del mundo si la elaboración del vino es chapucera.Es imposible detenerse en la cantidad de detalles que influyen en este proceso, pero su mínimo común denominador está rígidamente regulado en asociación al origen del vino, lo que nos lleva al siguiente punto.
