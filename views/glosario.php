    Lo cierto es que el mundo del vino está dominado por los expertos en marketing. Muchos términos que correctamente utilizados pueden ser útiles para describir un vino se han transformado en expresiones vacías, que sirven poco más que como reclamo publicitario.

    Hasta el vino de mesa de la peor calaña presume de “conjugar juventud y estructura”, y es que el papel lo aguanta (casi) todo. Sí, como decía el bueno de Bo Diddley, no se puede juzgar un libro por su portada, menos aún debemos fiarnos de las etiquetas de los vinos. Pero esto no quiere decir que no arrojen algunas pistas.
