        <div class="accordion-body">
          <strong>Variedad de la uva :</strong> El vino se hace exclusivamente con uva, por lo que el tipo de uva (o la mezcla de estas con las que elabora un vino, lo que se conoce como vino “multivarietal” o de coupage) es una de las pistas más importantes que vamos a encontrarnos. Suele indicarse en la etiqueta, aunque especificarlo no es obligatorio por ley, ni lo exigen todas las DO.
          Se calcula que existen unas 15.000 variedades diferentes de uva. Cada una de ellas puede existir en forma de varios cientos de clones ligeramente distintos, aunque suelen compartir características generales.
          Con la misma uva se pueden hacer vinos muy distintos, pero es una característica esencial para distinguir entre familias de cada tipo de vino.
