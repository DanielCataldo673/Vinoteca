          <strong>Maduración :</strong> El envejecimiento de los vinos, y las condiciones en que se realiza este, influye notablemente en el resultado final del vino. El vino nuevo tiene un sabor crudo y un aroma afrutado fuerte y simple. Cuando el vino reposa después de la fermentación se producen una serie de lentas reacciones químicas, cuyo resultado es el desarrollo de equilibrio y complejidad en el sabor. Influye, además, el lugar en el que se envejece el vino.El tiempo de envejecimiento de un vino influye notablemente en sus características organolépticas. Los vinos con más envejecimiento son, en general, más costosos de producir (pues deben permanecer más tiempo sin salir al mercado), pero no significan que sean mejores. Hay quien prefiere vinos jóvenes a vinos con mucha maduración, y hay buenos y malos ejemplos en todas las categorías de maduración.
