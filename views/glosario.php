        </button>
      </h3>
      <div id="collapseeleven" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
        <div class="accordion-body">
          <strong>Aroma :</strong> Es el conjunto de sensaciones olfativas que se desprenden del vino, que a su vez potencian el sabor de este. Aunque se suelen destacar solo los aromas positivos, los hay también negativos. Su listado es prácticamente interminable, prero podríamos distinguir varios grandes grupos de estos: afrutados, florales y vegetales (derivados de la uva), de panadería o lácticos (derivados del proceso de fermentación) y de madera o especias (derivados del proceso de envejecimiento).La definición del aroma es tremendamente subjetiva y se requiere un gran entrenamiento para ser un experto en captar todos los matices olfativos que desprende un vino, por lo que es un terreno abonado al flipadismo, también en las etiquetas. Ahora bien, correctamente identificados los aromas dan muchas pistas que nos ayudan a diferenciar los vinos e ir educando nuestro gusto.
          “Gran parte de las sensaciones que percibimos cuando bebemos son aromas, no sabores, aunque no nos demos cuenta”. “Olemos el vino en la boca, por vía retronasal. Por eso es imposible catar acatarrado, con la nariz congestionada. Resfriados la comida no nos sabe a nada por que somos incapaces de oler”.En opinión del experto, saber distinguir lo que es aroma de lo que es sabor en la boca es un paso importante para iniciarse en la cata.
