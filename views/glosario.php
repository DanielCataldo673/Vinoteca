      <div id="collapsetwelve" class="accordion-collapse collapse" data-bs-parent="#accordionExample">
        <div class="accordion-body">
          <strong>Sabor :</strong> El sabor de un vino está íntimamente ligado a su aroma, pero en boca se perciben matices distintos a los que capta nuestro olfato, para bien y para mal.Los cuatro sabores principales que se distinguen en el vino son ácido, dulce, salada y amargo, pero dentro de estos hay, de nuevo, infinidad de matices. En boca se puede percibir si un vino tiene aguja (un ligero picorcillo en la lengua, propio de los vinos jóvenes, que se suele considerar positivo), si es o no graso o es más o menos suave.Otros términos aplicados al sabor son más equívocos. Se dice, por ejemplo, que un vino es “profundo” o “complejo”, cuando presenta un amplio abanico de matices en boca, pero ninguna etiqueta calificará un vino como “austero”, palabra que se utiliza para designar a los vinos que carecen de matices, quizás por su excesiva juventud.
