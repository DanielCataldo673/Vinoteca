          <strong>Acidez :</strong> La acidez es un término que podría considerarse relativamente objetivo, pues un vino tiene un acidez determinada, que se mide en su pH, pero que en una cata se transforma en una característica organoléptica fundamental, cuya valoración no deja de ser subjetiva.Los ácidos presentes en la uva, sobre todo el tartático y el málico, que representan el 90 % del total, ayudan a impedir el crecimiento de bacterias indeseables durante la fermentación, proceso en el que se generan otros ácidos, como el láctico, succínico y acético.Los ácidos son un importante componente del sabor de un vino, pues, junto con los polifenoles, contrarresta el sabor dulce del etanol, pero deben estar presentes en su justa medida.
