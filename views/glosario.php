          <strong>Territorio :</strong> Hablamos de territorio (o terroir, si eres un snob) para distinguir particularidades de parajes concretos situados en una misma área: la influencia sobre el vino del lugar concreto en que se cultivan las uvas. El“territorio” se refiere a todo el entorno físico del viñedo: el suelo, su estructura y contenido de minerales; el agua que hay en el suelo; la elevación, la inclinación y orientación del viñedo, y el microclima, el régimen de temperaturas, sol, humedad y precipitaciones.Hay ciertas características del territorio que se indican en las etiquetas que son, a priori, positivas, como la pendiente de la ladera o la altitud, que dificultan el crecimiento de las uvas y las hacen más sabrosas.Otra característica positiva de un vino es que las uvas provengan de una sola finca, con unas características mucho más controladas que los vinos de varias fincas. También influye en la calidad el vino la edad de las vides: un viñedo viejo produce poca uva pero, a priori, de mayor calidad. Es una característica que se suele destacar en la etiqueta.
