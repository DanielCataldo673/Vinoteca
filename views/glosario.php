          <strong>Equilibrio :</strong> Se dice que un vino es “equilibrado”, “estructurado” o “elegante" cuando los aromas y sabores se ajustan correctamente y no hay nada que desentone en el conjunto. Se trata de palabras que no indica absolutamente nada hasta que no probamos un vino, pues ningún bodeguero o sumiller va a indicar que el vino que trata de vender no está equilibrado.
        </div>
