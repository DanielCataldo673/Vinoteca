          <strong>Maridaje :</strong> El maridaje se refiere al tipo de comida que encaja con cada vino en concreto. En las etiquetas se suele indicar qué tipo de alimentos acompañan mejor al producto, pero hasta el peor vino de mesa tiene sus alimentos predilectos, que quizás van mejor con cualquier otro vino que, sencillamente, esté más rico.Por supuesto, hay comidas que encajan mejor con distintos vinos, pero es un terreno lleno de mitos e ideas preconcebidas, que no siempre están justificadas, como que los tintos van con la carne y los blancos con el pescado. No siempre es así, y habría que analizar cada vino y plato concreto para elegir combinaciones satisfactorias, que dependen, como todo, del gusto de cada cual.
