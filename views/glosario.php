          <strong>Astrigencia :</strong> Los taninos y otros compuestos fenólicos presentes en la uva aportan una sensación astringente al vino, y le dan cuerpo y peso. Se dice que un vino es “astringente” cuando los taninos están demasiado presentes, lo que provoca una una sensación de roce entre la lengua y el paladar, ya que se coagula la saliva y se pierde lubricación en la boca.
        </div>
      </div>
