        <div class="accordion-body">
          <h5 class="text-decoration-underline fst-italic">Palabras </h5>
          <strong>Délestage</strong> : Es una técnica de trabajo del mosto o del vino que permite de optimizar los cambios (intercambios) entre la fase líquida y la fase sólida durante la maceración.
          <strong>Despapillado</strong> : Consiste en separar las uvas del raspón o escobajo que es la estructura herbácea del racimo.
          <br>
          <strong>Las levaduras autóctonas o nativas</strong> : Provienen del viñedo y se adhieren a la capa cerosa de las uvas, llamada pruina, y gracias a ellas se podrá realizar la fermentación alcohólica de manera espontánea asegurando unas condiciones óptimas para su correcto desarrollo.
          <br>
          <strong>Bazuqueo (Pisoneo o Pigeage)</strong> : Se trata de romper y hundir el sombrero para mezclarlo con el mosto (con ayuda de un bazuqueador) para aumentar su maceracion. Este proceso que se aplica generalmente en cepas como el Pinot Noir, también ayuda a evitar la oxidación y acetificación del sombrero.
          <br>
          <strong>Sombrero</strong> : Una vez que la fermentación alcohólica ocurre, las levaduras desprenden gas carbónico que empuja las pieles hacia arriba formando una capa que flota sobre el vino, llamada sombrero.
          <br>
          <strong>Fermentación maloláctica</strong> : Consiste en la transformación del ácido málico en ácido láctico por medio de bacterias que de forma natural se encuentran en la propia uva y por tanto también en el fino recién fermentado.
          <br>
          <strong>Lías</strong> : Las lías son microorganismos, principalmente levaduras, encargadas de realizar la fermentación alcohólica, y en menor grado bacterias, sobre todo si el vino no realizó la fermentación maloláctica, que al terminar su actividad, se mueren y se van descomponiendo, proceso conocido como autólisis.

