    <p class="text-center mt-4 lh-lg fw-medium fst-italic fs-5"> <!-- <font face="Comic Sans MS"> -->El área de investigación y desarrollo fue fundada en el año 2009, luego de nuestra llegada a Valle de Uco, con el objetivo de analizar y controlar todas las etapas del proceso productivo, desde el viñedo hasta la bodega.
      Desde el comienzo trabajamos en la clasificación de diferentes microrregiones del Valle de Uco con diversos tipos de suelos, en los que perfeccionamos todas las prácticas de viñedo, como los periodos de riego, la utilización de diferentes sistemas de conducción, las variedades que se adaptan mejor, el estilo de vino que se puede obtener y el momento óptimo de cosecha.
      Uno de los principales objetivos fue preservar todas las características y diversidad, que obtenemos en cada región y viñedo, en la bodega. Por eso fue muy importante la creación de vasijas que nos permitieran vinificar con la menor intervención posible y de manera sustentable
      Hoy en nuestra bodega utilizamos únicamente vasijas de concreto sin epoxi, en los formatos troncocónico y ánfora.
