    <p class="text-center mt-4 lh-lg fw-medium fst-italic fs-5">Hoy Sebastián, tercera generación de la familia, se encuentra trabajando al frente de un joven equipo agrícola y enológico, encargado de producir vinos con los más altos estándares de calidad en el Valle de Uco.
      Por iniciativa suya, desde 2008 la bodega cuenta con un área de Investigación y Desarrollo, dedicada al estudio del terroir y las diversas variables que inciden en la calidad del vino. El objetivo es, según palabras del propio Sebastián “no buscar
      vinos perfectos, sino aquellos que expresen el lugar, la región”.
